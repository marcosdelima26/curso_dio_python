# operadores de associção: São operadores utilizados para verificar se um objeto está presente em uma sequência. 

curso = "Curso de Python"
frutas = ["laranja", "uva", "limão"]
saque = [1500, 100]

print("Python" in curso)
print("Maçã" not in frutas)
print(200 in saque)

# operador de associação in verifica se algo está em uma determinada sequência. O operador in é case sensitive (Diferencia letra maiúscula de minúscula, ou seja, se passar o valor Laranja com L maiúsculo e na lista estiver minúsculo ele não ira retornar um valor verdadeiro.)


