# Tuplas: São estruturas de dados muito parecidas com as listas, a principal diferença é que tuplas são imutáveis enquanto listas são mutáveis. Podemos criar tuplas através da classe tuple, ou colocando valor separados por vírgula de parenteses. 

frutas = ("laranja", "pera", "uva",)

letras = tuple("python")

numeros = tuple([1, 2, 3, 4])

pais = ("Brasil",)


# Uma boa prática para que o python possa entender que se trata de uma tupla é por uma vírgula no final. 



