# Soma
print(10 + 10) # resultado 20

# Subtração
print(10 - 10) # Resultado 0

# Multiplicação
print(10 * 10) # Resultado 100

# Divisão
print(10 / 10) # Resultado 1.0 (Retorna na divisão um número do tipo float)

# Divisão inteira
print(5 // 2) # Resultado 2

# Módulo
print(10 % 3) # Resultado 1 (Retorna o resto da divisão)

# Exponenciação
print(2 ** 3) # Resultado 8

# Precedência
# 1º Se resolve Parênteses
# 2º Expoêntes
# 3º Multiplicação e divisão (Da esquerda para a direita)
# 4º Soma e Subtração (Da esquerda para a direita)
