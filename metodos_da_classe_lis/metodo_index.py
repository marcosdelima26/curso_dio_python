# No método index, podemos saber onde é a primeira ocorrência desse objeto python (em que índice está)

linguagens = ["cobol", "python", "js", "c", "java", "csharp", "python"]

print(linguagens.index("java"))
print(linguagens.index("python"))

# Obs: O objeto python está duplicado na lista, entretanto ele irá apresentar o resultado 1, pois o primeiro índice que corresponde ao objeto python esta no índice 1 não apresentado o outro objeto. 