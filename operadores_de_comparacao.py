# Operadores de comparação serve para fazer comparações entre dois valores.

# A comparação é feita pelo == ou != quando desejo verificar se é diferente.

# Após a comparação será retornado um valor Booleano (True ou False)

# Operadores de comparação
# == Igual á
# != Diferente de
# > Maior que
# < Menor que
# >= Maior ou igual a
# <= Menor ou igual a 

saldo = 450
saque = 200

print(saldo == saque)   # resultado False
print(saldo != saque)   # Resultado True
print(saldo > saque)    # Resultado True
print(saldo >= saque)   # Resultado True
print(saldo < saque)    # Resultado False
print(saldo <= saque)   # Resultado False
