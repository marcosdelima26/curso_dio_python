# Operadores de atribuição = "São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável."

# Atribuição simples (=)
saldo = 500
print(f'Atribução simples resultado = {saldo}') # Resultado 500

# Atribução com adição
saldo = 500
saldo += 200
print(f'Atribuição com adição resultado = {saldo}' ) # Resultado 700

# Atribuição com subtração
saldo = 500
saldo -= 200
print(f'Atribuição com subtração resultado = {saldo}') # Resultado 300

# Atribuição com Multiplicação 
saldo = 500
saldo *= 2
print(f'Atribuição com Multiplicação resultado = {saldo}') # Resultado 1000

# Atribução com Divisão
saldo = 500
saldo /= 5
print(f'Atribuição com divisão resultado = {saldo}') # Resultado 100.0

# Atribução com Divisão inteira
saldo = 500
saldo //= 5
print(f'Atribuição com Divisão inteira resultado = {saldo}') # Resultado 100

# Atribuição com Módulo
saldo = 500
saldo %= 480
print(f'Atribuição com Módulo resultado = {saldo}') # Resultado 20

# Atribuição com Exponenciação
saldo = 80
saldo **= 2
print(f'Atribuição com Exponenciação = {saldo}') # Resultado 6400
